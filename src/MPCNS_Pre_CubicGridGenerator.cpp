int GetIntOrDefault(Param &param, const std::string &name, int default_value)
{
    return param.HasInt(name) ? param.GetInt(name) : default_value;
}

double GetDoubleOrDefault(Param &param, const std::string &name, double default_value)
{
    return param.HasDou(name) ? param.GetDou(name) : default_value;
}

std::string GetStringOrDefault(Param &param, const std::string &name, const std::string &default_value)
{
    return param.HasStr(name) ? param.GetStr(name) : default_value;
}

void EnsureInputDirectory()
{
    if (mkdir("INPUT", 0755) != 0 && errno != EEXIST)
    {
        std::cout << "#Error Writing: can't create directory : \tINPUT" << std::endl;
        exit(-1);
    }
}

void WriteGridFile(const std::string &filename, int nx, int ny, int nz,
                   double xmin, double xmax, double ymin, double ymax, double zmin, double zmax)
{
    std::ofstream file(filename.c_str(), std::ios_base::out);
    if (!file)
    {
        std::cout << "#Error Writing: can't open file : \t" << filename << std::endl;
        exit(-1);
    }

    file << std::setprecision(16);
    file << 1 << "\n";
    file << nx << "\t" << ny << "\t" << nz << "\n";

    for (int k = 0; k < nz; ++k)
        for (int j = 0; j < ny; ++j)
            for (int i = 0; i < nx; ++i)
            {
                const double ratio = (nx == 1) ? 0.0 : static_cast<double>(i) / static_cast<double>(nx - 1);
                file << xmin + (xmax - xmin) * ratio << "\n";
            }

    for (int k = 0; k < nz; ++k)
        for (int j = 0; j < ny; ++j)
            for (int i = 0; i < nx; ++i)
            {
                const double ratio = (ny == 1) ? 0.0 : static_cast<double>(j) / static_cast<double>(ny - 1);
                file << ymin + (ymax - ymin) * ratio << "\n";
            }

    for (int k = 0; k < nz; ++k)
        for (int j = 0; j < ny; ++j)
            for (int i = 0; i < nx; ++i)
            {
                const double ratio = (nz == 1) ? 0.0 : static_cast<double>(k) / static_cast<double>(nz - 1);
                file << zmin + (zmax - zmin) * ratio << "\n";
            }
}

