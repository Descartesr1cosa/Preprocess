bool CubicPeriodicGridGenerator::IsEnabled(Param &param)
{
    return param.HasBoo("generate_cubic_periodic_grid") && param.GetBoo("generate_cubic_periodic_grid");
}

void CubicPeriodicGridGenerator::GenerateIfEnabled(Param &param)
{
    if (!IsEnabled(param))
        return;

    const int nx = GetIntOrDefault(param, "cubic_nx", 9);
    const int ny = GetIntOrDefault(param, "cubic_ny", 9);
    const int nz = GetIntOrDefault(param, "cubic_nz", 9);
    if (nx < 2 || ny < 2 || nz < 2)
    {
        std::cout << "#Fatal Error: cubic_nx/cubic_ny/cubic_nz must be >= 2 for periodic faces." << std::endl;
        exit(-1);
    }

    const double xmin = GetDoubleOrDefault(param, "cubic_xmin", 0.0);
    const double xmax = GetDoubleOrDefault(param, "cubic_xmax", 1.0);
    const double ymin = GetDoubleOrDefault(param, "cubic_ymin", 0.0);
    const double ymax = GetDoubleOrDefault(param, "cubic_ymax", 1.0);
    const double zmin = GetDoubleOrDefault(param, "cubic_zmin", 0.0);
    const double zmax = GetDoubleOrDefault(param, "cubic_zmax", 1.0);
    const std::string block_name = GetStringOrDefault(param, "cubic_block_name", "Fluid");

    EnsureInputDirectory();
    WriteGridFile(param.GetStr("gfilename"), nx, ny, nz, xmin, xmax, ymin, ymax, zmin, zmax);
    WriteInpFile(param.GetStr("bfilename"), nx, ny, nz, block_name);
    WriteBoundaryNameFile(param.GetStr("ffilename"));

    param.AddParam("grd_readtype", 0);
    param.AddParam("dimension", 3);

    if (!param.HasBoo("if_split_group_info"))
        param.AddParam("if_split_group_info", false);

    std::cout << "\t-->Cubic periodic test grid has been generated in ./INPUT ! ! !\n";
}
